/** Uniform 5-step reasoning pipeline; content varies per dashboard. */
function mkSteps(p: {
  understand: string;
  citations: Citation[];
  analyze: string;
  validate: string;
}): ReasoningStep[] {
  return [
    {
      kind: "reason",
      title: "درک داده‌های داشبورد",
      detail: "شناسایی شاخص‌های کلیدی و هدف تحلیل",
      durationMs: 2600,
      reason: p.understand,
    },
    {
      kind: "retrieve",
      title: "ارجاع به اسناد استاندارد",
      detail: "تطبیق مقادیر با رنج‌های مرجع",
      durationMs: 2400,
      citations: p.citations,
    },
    {
      kind: "reason",
      title: "تحلیل روندها و انحراف‌ها",
      detail: "شناسایی الگوها و نقاط بحرانی",
      durationMs: 2800,
      reason: p.analyze,
    },
    {
      kind: "validate",
      title: "اعتبارسنجی و تحلیل حساسیت",
      detail: "بررسی پایداری نتیجه",
      durationMs: 2300,
      reason: p.validate,
    },
    {
      kind: "synthesize",
      title: "تولید بینش و راهکار",
      detail: "جمع‌بندی نهایی",
      durationMs: 1500,
    },
  ];
}

